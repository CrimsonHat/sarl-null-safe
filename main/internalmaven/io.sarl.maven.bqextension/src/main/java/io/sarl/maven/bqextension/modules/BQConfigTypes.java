package io.sarl.maven.bqextension.modules;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

import io.sarl.maven.bqextension.configs.Config;

/** Annotation for specifying the types of the configs.
 *
 * @author $Author: sgalland$
 * @version $FullVersion$
 * @mavengroupid $GroupId$
 * @mavenartifactid $ArtifactId$
 * @since 0.8
 */
@Target({ElementType.TYPE})
@Retention(RetentionPolicy.RUNTIME)
public @interface BQConfigTypes {

	/**
	 * The types of the configurations that are linked to the annoted type.
	 *
	 * @return the types.
	 */
	Class<? extends Config>[] value() default {};

}
